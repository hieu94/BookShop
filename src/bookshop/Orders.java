    public void RetrieveData(String sql) {
        String header[] = {"Order ID", "Customer ID", "Order date","Book ID", "Total"};
        DefaultTableModel tblModel = new DefaultTableModel(header, 0);
        try {
            Vector data = null;
            tblModel.setRowCount(0);
            ConnectionToDatabase kn = new ConnectionToDatabase();
            ResultSet rs = kn.Query(sql);
            while (rs.next()) {
                data = new Vector();
                data.add(rs.getString("Order ID"));
                data.add(rs.getString("Customer ID"));
                data.add(rs.getString("Order date"));
                data.add(rs.getString("Book ID"));
                data.add(rs.getDouble("Total"));
                
                tblModel.addRow(data);
            }
        tableOrders.setModel(tblModel);
        } catch (Exception ex) {
            System.out.println("Error: " + ex);
        }
    }
    
