    private void btnDeleteOrderActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteOrderActionPerformed
        int dialogButton = JOptionPane.YES_NO_OPTION;
        int dialogResult = JOptionPane.showConfirmDialog(null, "Confirm to delete order?", "Delete Order", dialogButton);
        if (dialogResult == JOptionPane.YES_OPTION) {
            String orderID;
            orderID = tableOrders.getValueAt(tableOrders.getSelectedRow(), 0).toString();
            String sql = "DELETE FROM [Order] WHERE [Order ID] = N'" + orderID + "'";
            ConnectionToDatabase ketNoi = new ConnectionToDatabase();
            ketNoi.Update(sql);
            sql = "SELECT * FROM [Order]";
            RetrieveData(sql);
        }
    }//GEN-LAST:event_btnDeleteOrderActionPerformed

